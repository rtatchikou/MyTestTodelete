package com.efunzo.be.training;

public class OperatingRoom {
	
	private int product= 10;

	public OperatingRoom(int product) {

		if(product<100){
			
		   this.product = product;
		}
	}
	

	@Override
	public String toString() {
		return "OperatingRoom [product=" + product + "]";
	}

	public int getProduct() {
		return product;
	}

	public void getHealth() {
		if(this.product > 100){
			System.out.println("Your patient has die");
		}else {
			System.out.println("Your patient is alive");
		}
	}
	

}
